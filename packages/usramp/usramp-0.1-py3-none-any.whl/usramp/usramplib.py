def centroid(moleculein):
    natoms=moleculein.n
    xc, yc, zc = 0.0, 0.0, 0.0
    for iatom in moleculein.atoms:
        xc=xc+(iatom.xc)
        yc=yc+(iatom.yc)
        zc=zc+(iatom.zc)
    vectorc=np.array([xc, yc, zc])/float(natoms)
    return vectorc
