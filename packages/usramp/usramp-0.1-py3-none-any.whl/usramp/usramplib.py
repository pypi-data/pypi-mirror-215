def shape_recognition(moleculeina,moleculeinb):
    v0=descriptors(moleculeina)
    v1=descriptors(moleculeinb)
    list=[np.absolute(a-b) for a,b in zip(v0,v1)]
    s=1.0/(1.0+sum(list)/24.0)
    return s
