def first3_central_moment(listxxx):
    xavg=np.mean(listxxx)
    res = listxxx - xavg
    ssq1 = np.mean(res) 
    ssq2 = np.mean(res**2) 
    ssq3 = np.mean(res**3) 
    return [ssq1,ssq2,ssq3]
